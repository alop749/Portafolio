// State Management
let currentLanguage = 'es';

// UI Elements
const loader = document.getElementById('loader');
const navbar = document.getElementById('navbar');
const mobileToggle = document.getElementById('mobile-toggle');
const navLinks = document.getElementById('nav-links');
const langBtn = document.getElementById('lang-btn');
const langDropdown = document.getElementById('lang-dropdown');
const currentLangText = document.getElementById('current-lang');
const revealElements = document.querySelectorAll('.reveal');
const contactForm = document.getElementById('contact-form');

// 1. Initial Loader Logic
window.addEventListener('load', () => {
    setTimeout(() => {
        loader.classList.add('loader-hidden');
    }, 1000);
});

// 2. Language Switcher Logic
langBtn.addEventListener('click', (e) => {
    e.stopPropagation();
    langDropdown.classList.toggle('active');
});

document.addEventListener('click', () => {
    langDropdown.classList.remove('active');
});

langDropdown.querySelectorAll('button').forEach(btn => {
    btn.addEventListener('click', () => {
        const lang = btn.getAttribute('data-lang');
        setLanguage(lang);
    });
});

function setLanguage(lang) {
    currentLanguage = lang;
    currentLangText.textContent = lang.toUpperCase();

    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update form placeholders specifically
    if (lang === 'es') {
        document.getElementById('name').placeholder = "Tu nombre...";
        document.getElementById('email').placeholder = "tu@email.com";
        document.getElementById('message').placeholder = "¿En qué puedo ayudarte?";
    } else if (lang === 'en') {
        document.getElementById('name').placeholder = "Your name...";
        document.getElementById('email').placeholder = "you@email.com";
        document.getElementById('message').placeholder = "How can I help you?";
    }
    // Add other placeholders if needed
}

// 3. Navbar Sticky Effect
window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
        navbar.classList.add('scrolled');
    } else {
        navbar.classList.remove('scrolled');
    }
});

// 4. Scroll Reveal Logic
const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('active');
        }
    });
}, { threshold: 0.1 });

revealElements.forEach(el => revealObserver.observe(el));

// 5. Mobile Menu Logic
mobileToggle.addEventListener('click', () => {
    mobileToggle.classList.toggle('active');
    navLinks.classList.toggle('active');
});

navLinks.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', () => {
        mobileToggle.classList.remove('active');
        navLinks.classList.remove('active');
    });
});

// 6. Contact Form Handling (AJAX Submission)
contactForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const data = new FormData(contactForm);
    const submitBtn = contactForm.querySelector('button');
    const originalText = submitBtn.textContent;

    submitBtn.textContent = currentLanguage === 'es' ? "Enviando..." : "Sending...";
    submitBtn.disabled = true;

    try {
        const response = await fetch(contactForm.action, {
            method: contactForm.method,
            body: data,
            headers: {
                'Accept': 'application/json'
            }
        });

        if (response.ok) {
            alert(currentLanguage === 'es' ? "¡Mensaje enviado con éxito!" : "Message sent successfully!");
            contactForm.reset();
        } else {
            const errorData = await response.json();
            alert(currentLanguage === 'es' ? "Hubo un problema al enviar el mensaje. Inténtalo de nuevo." : "There was a problem sending your message. Please try again.");
            console.error(errorData);
        }
    } catch (error) {
        alert(currentLanguage === 'es' ? "Error de conexión. Por favor verifica tu internet." : "Connection error. Please check your internet.");
        console.error(error);
    } finally {
        submitBtn.textContent = originalText;
        submitBtn.disabled = false;
    }
});

// 7. Active Navigation Highlighting
const sections = document.querySelectorAll('section');
window.addEventListener('scroll', () => {
    let current = '';
    sections.forEach(section => {
        const sectionTop = section.offsetTop;
        const sectionHeight = section.clientHeight;
        if (pageYOffset >= (sectionTop - 200)) {
            current = section.getAttribute('id');
        }
    });

    navLinks.querySelectorAll('a').forEach(a => {
        a.classList.remove('active-nav');
        if (a.getAttribute('href').includes(current)) {
            a.classList.add('active-nav');
        }
    });
});
